#ifndef MENU_HPP
#define MENU_HPP

/**
 * @file menu.hpp
 * @brief Declaração da classe menu.
 *
 * Esta classe contém métodos para exibir diferentes telas do menu do jogo.
 */

/**
 * @class menu
 * @brief Classe para exibição do menu do jogo.
 *
 * A classe menu contém métodos para exibir a tela inicial, as instruções e o objetivo do jogo.
 */
class menu {
public:
    /**
     * @brief Método para exibir a tela inicial do jogo.
     */
    void TELA_INICIAL();

    /**
     * @brief Método para exibir as instruções do jogo.
     */
    void INSTRUCOES();

    /**
     * @brief Método para exibir o objetivo do jogo.
     */
    void OBJETIVO();
};

#endif // MENU_HPP
